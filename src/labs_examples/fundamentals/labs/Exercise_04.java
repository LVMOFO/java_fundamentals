package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        int i = 1;
        System.out.println("int i is: " + i);

        // write your code below
        int iInt = 4;
        byte bByte = 1;
        short sShort = 32767;
        long lLong = 8000000000000000000l;
        float fFloat = 9.1234567f;
        double dDouble = 8.012345678901234;
        boolean bBoolean = true;
        char cChar = '2';

        System.out.println("iInt = " + iInt);
        System.out.println("bByte = " + bByte);
        System.out.println("sShort = " + sShort);
        System.out.println("lLong = " + lLong);
        System.out.println("fFloat = " + fFloat);
        System.out.println("dDouble = " + dDouble);
        System.out.println("bBoolean = " + bBoolean);
        System.out.println("cChar = " + cChar);

    }

}
